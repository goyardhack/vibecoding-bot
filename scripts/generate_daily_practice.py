THEMES = [
    "промпты и AI",
    "Cursor и инструменты",
    "вёрстка и лендинги",
    "Telegram-боты",
    "монетизация",
    "деплой и инфраструктура",
    "продуктовое мышление",
    "практика и закрепление",
]


def pick_difficulty(day: int) -> str:
    if day % 7 in (0, 6):
        return "hard"
    if day % 3 == 0:
        return "medium"
    return "easy"


def build_days() -> list[dict]:
    days: list[dict] = []
    easy_i = medium_i = hard_i = 0

    for day in range(1, 366):
        difficulty = pick_difficulty(day)
        if difficulty == "easy":
            title, task = EASY[easy_i % len(EASY)]
            easy_i += 1
        elif difficulty == "medium":
            title, task = MEDIUM[medium_i % len(MEDIUM)]
            medium_i += 1
        else:
            title, task = HARD[hard_i % len(HARD)]
            hard_i += 1

        theme = THEMES[(day - 1) % len(THEMES)]
        task = f"[Тема: {theme}]\n\n{task}"

        days.append(
            {
                "day": day,
                "difficulty": difficulty,
                "title": title,
                "task": task,
            }
        )

    return days


def main() -> None:
    data = {"days": build_days()}
    OUTPUT.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Written {len(data['days'])} days to {OUTPUT}")


if __name__ == "__main__":
    main()
